    private void cargarDocentesEnComboBox() {
        comboDocentes.removeAllItems();
        comboDocentes.addItem("Elegir Docente"); // Opción por defecto

        java.util.List<String> docentes = DocenteController.obtenerDocentes();
        for (String d : docentes) {
            String nombre = d.split("\\|")[0];
            comboDocentes.addItem(nombre);
        }
    }

