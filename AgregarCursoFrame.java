    private void btnCrearCursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearCursoActionPerformed
        String nombre = txtNombre.getText().trim();
        String codigo = txtCodigo.getText().trim();
        String precioStr = txtPrecio.getText().trim();
        String docente = (String) comboDocentes.getSelectedItem();

        if (codigo.isEmpty() || nombre.isEmpty() || precioStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No puede haber campos vacios", "Campo Obligatorio", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (docente == null || docente.equals("Elegir Docente")) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar un docente válido para el curso.", "Error de Selección", JOptionPane.WARNING_MESSAGE);
            return;
        }
        double precio;
        try {
            precio = Double.parseDouble(precioStr);
            if (precio < 0) {
                JOptionPane.showMessageDialog(this, "El precio no puede ser un valor negativo.", "Precio Inválido", JOptionPane.WARNING_MESSAGE);
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio ingresado no es un número válido. Por favor, ingrese solo números y decimales (ej. 12.50).", "Precio Inválido", JOptionPane.WARNING_MESSAGE);
            return;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al procesar el precio: " + e.getMessage(), "Error Inesperado", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String mensajeError = CursoController.agregarCurso(codigo, nombre, docente, precio);
        
        if (mensajeError == null) {
            JOptionPane.showMessageDialog(this, "Curso creado correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            txtCodigo.setText("");
            txtNombre.setText("");
            txtPrecio.setText("");
            comboDocentes.setSelectedIndex(0);
            lblMensaje.setText(" ");
            
        } else {
            JOptionPane.showMessageDialog(this, mensajeError, "Error al Crear Curso", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnCrearCursoActionPerformed

