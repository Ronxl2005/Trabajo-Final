    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
        int[] filas = tablaCursos.getSelectedRows();
        if (filas.length < 1 || filas.length > 5) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar entre 1 y 5 cursos.");
            return;
        }

        List<String> seleccionados = new java.util.ArrayList<>();
        for (int fila : filas) {
            String codigo = (String) tablaCursos.getValueAt(fila, 0);
            String nombre = (String) tablaCursos.getValueAt(fila, 1);
            String docente = (String) tablaCursos.getValueAt(fila, 2);
            String precio = (String) tablaCursos.getValueAt(fila, 3);
            seleccionados.add(codigo + "|" + nombre + "|" + docente + "|" + precio);
        }

        if (seleccionados.size() < 1 || seleccionados.size() > 5) {
            JOptionPane.showMessageDialog(this, "Debes seleccionar entre 1 y 5 cursos.");
            return;
        }

        double total = 0;
        for (String c : seleccionados) {
            String[] p = c.split("\\|");
            // Esta es la línea clave: reemplaza la coma por punto directamente en la cadena del precio
            total += Double.parseDouble(p[3].replace(',', '.'));
        }
        new PagoFrame(username, nombreCompleto, seleccionados, total).setVisible(true);
    }//GEN-LAST:event_btnSeleccionarActionPerformed

    private void btnVerMatriculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerMatriculaActionPerformed
        List< String> cursos = MatriculaController.cursosMatriculados(username);

        if (cursos.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Aún no tienes cursos matriculados.");
        } else {
            String mensaje = "Cursos matriculados:\n";
            for (String c : cursos) {
                mensaje += "- " + c.split("\\|")[1] + "\n";
            }
            JOptionPane.showMessageDialog(this, mensaje);
        }

    }//GEN-LAST:event_btnVerMatriculaActionPerformed

