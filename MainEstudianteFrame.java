    private void cargarCursos() {
        List<String> cursos = CursoController.obtenerCursos(); // Formato: código|nombre|docente|precio
        String[][] datos = new String[cursos.size()][4];

        for (int i = 0; i < cursos.size(); i++) {
            String[] partes = cursos.get(i).split("\\|");
            datos[i][0] = partes[0];
            datos[i][1] = partes[1];
            datos[i][2] = partes[2];
            datos[i][3] = partes[3];
        }

        String[] titulos = {"Código", "Curso", "Docente", "Precio"};
        tablaCursos.setModel(new javax.swing.table.DefaultTableModel(datos, titulos) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        });
    }

